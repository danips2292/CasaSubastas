    private void btn_Aceptar_Habilitar_AgenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Aceptar_Habilitar_AgenteActionPerformed
        // TODO add your handling code here:
           if(this.TextField_ID_Agente_Habilitar.getText().equals("")){
            JOptionPane.showMessageDialog(null, "FALTA ESCRIBIR EL ID DEL AGENTE QUE DESEA HABILITAR", "VERIFICAR", JOptionPane.WARNING_MESSAGE);
            this.TextField_ID_Agente_Habilitar.requestFocus();
        }
        else{
             try {
                int k = JOptionPane.showConfirmDialog(null, "DESEA GUARDAR LOS DATOS DEL USUARIO?", "PREGUNTA", JOptionPane.YES_NO_OPTION);
                if (k == JOptionPane.YES_OPTION) {
                    try (
                        PreparedStatement pstm = con.Conectar().prepareStatement("{call admi_habilitarAgente (?)}")) {
                        pstm.setString(1, TextField_ID_Agente_Habilitar.getText());
                        //convertirlo a binarios
                        ResultSet r = pstm.executeQuery();
                        //pstm.executeUpdate();
                        System.out.println();
                        String respuesta = "";
                        while (r.next()) {
                            respuesta = r.getString(1).toString();
                        }
                        JOptionPane.showMessageDialog(null, respuesta, "CONFIRMACION", JOptionPane.WARNING_MESSAGE);

                    }
                    }
            } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "ERROR AL RESGISTRAR", "ERROR", JOptionPane.WARNING_MESSAGE);
                     System.out.println("Error Message: " + e.getSQLState());
                     System.out.println("SQLStateCode: "  + e.getMessage());
                     System.out.println("ErrorCode: "     + e.getErrorCode());
                }
        }
    }//GEN-LAST:event_btn_Aceptar_Habilitar_AgenteActionPerformed

